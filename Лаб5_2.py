seasons = int(input("Enter the number from 1 to 4:   "))

if seasons == 1:
    print("Winter")

if seasons == 2:
    print("Spring")

if seasons == 3:
    print("Summer")

if seasons == 4:
    print("Autumn")


if seasons > 4:
     print("Error")


if seasons < 1:
     print("Error")
